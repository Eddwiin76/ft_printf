#include "libft.h"

t_list	*ft_lstnew(void *content)
{
	t_list *list;

	list = NULL;
	if (!(list = (t_list *)malloc(sizeof(t_list))))
		return (NULL);
	list->content = content;
	list->next = NULL;
	return (list);
}
